    public void preencheTabela(){
        
 
        DefaultTableModel modelTabela = new DefaultTableModel(new Object[]{"Data", "Descrição", "Tipo", "Valor Pendente"}, 0);
        
        List<CarCapContas> contasPendentes = new CarCapContasDAO().ListarLancamentosFuturos(txtdataIni.getDate(), txtDataFim.getDate());

        for(CarCapContas i : contasPendentes){
            
            Object[] data = {Utils.formatData(i.getContaDataEmissao()), i.getDescricao(), i.getContaTipo().toString(), Utils.format((i.getContaValorTotal() - i.getContaValorPago()))};
            modelTabela.addRow(data);
        }
        
          tableLancFuturos.setModel(modelTabela);
  
        
    }

